package exercises;

public class Arithmetic {
    
    public float add(float x, float y)
{
    float result = x + y;
    return result;
    }

public float sub(float x, float y){
    float result = x - y;
    return result;
}

public float mult(float x, float y){
    float result = x * y;
    return result;
}

public float div(float x, float y){
    float result = x / y;
    return result;
}

}
